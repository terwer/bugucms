package com.terwergreen.bugucms.dto;

public class SiteConfigDTO {
    private String domain;
    private String weburl;
    private String webtheme;
    private String webname;
    private String webslogen;
    private String keywords;
    private String description;
    private String beianinfo;
    private String debug;
    private String adminpath;

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

    public String getWeburl() {
        return weburl;
    }

    public void setWeburl(String weburl) {
        this.weburl = weburl;
    }

    public String getWebtheme() {
        return webtheme;
    }

    public void setWebtheme(String webtheme) {
        this.webtheme = webtheme;
    }

    public String getWebname() {
        return webname;
    }

    public void setWebname(String webname) {
        this.webname = webname;
    }

    public String getWebslogen() {
        return webslogen;
    }

    public void setWebslogen(String webslogen) {
        this.webslogen = webslogen;
    }

    public String getKeywords() {
        return keywords;
    }

    public void setKeywords(String keywords) {
        this.keywords = keywords;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getBeianinfo() {
        return beianinfo;
    }

    public void setBeianinfo(String beianinfo) {
        this.beianinfo = beianinfo;
    }

    public String getDebug() {
        return debug;
    }

    public void setDebug(String debug) {
        this.debug = debug;
    }

    public String getAdminpath() {
        return adminpath;
    }

    public void setAdminpath(String adminpath) {
        this.adminpath = adminpath;
    }
}
